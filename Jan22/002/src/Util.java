/**
 * This is Util
 */
public class Util {
    
    /**
     * This method returns the sum of two ints
     * 
     * @param a first int
     * @param b secound int
     * @return sum of two ints
     */
    public static int addTwoNumbers(int a, int b) {
        return a + b;
    }

    /**
     * 
     * @see {@link https://www.oracle.com/ca-en/technical-resources/articles/java/javadoc-tool.html}
     * @see {@code int a = 10} 
     * @see {@link Util}
     * 
     * @param a
     * @param b
     * @return
     */
    public static double addTwoNumbers(double a, double b) {
        return a + b;
    }

    /**
     * 
     * 
     * @param a
     * @param b
     * @return
     */
    public static float addTwoNumbers(float a, float b) {
        return a + b;
    }

    

}
